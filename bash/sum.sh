#!/bin/bash

sum() {
# Принимает 2 аргумента - числа
# Проверяет, что оба аргумента переданы
# Проверяет, что оба аргумента - целые числа
# Складывает их и выводит результат
# Возвращает 0 при успехе, 1 при ошибке

  # Проверка числа аргументов
  if [ $# -ne 2 ]; then
    echo "Ошибка: нужно 2 аргумента"
    return 1
  fi

  # Проверка целых чисел для $1 и $2
  if ! [[ "$1" =~ ^-?[0-9]+$ ]]; then
    echo "Ошибка: $1 не целое число"
    return 1
  fi

  if ! [[ "$2" =~ ^-?[0-9]+$ ]]; then
    echo "Ошибка: $2 не целое число"
    return 1
  fi

  # Сложение
  local result=$(( $1 + $2 ))
  echo "$result"
  return 0
}

# Тест
sum 5 3      # Вывод: 8, код 0
sum -2 7     # Вывод: 5, код 0
sum abc 5    # Ошибка, код 1
sum 5        # Ошибка, код 1