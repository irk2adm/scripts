#!/bin/bash

# Принимает число как аргумент
# Проверяет, чётное оно или нечётное
# Выводит соответствующее сообщение

# Проверка аргумента
if [ -z "$1" ]; then
    echo "Использование: $0 <число>"
    exit 1
fi

# Проверка на целое число
if ! [[ "$1" =~ ^-?[0-9]+$ ]]; then
    echo "Ошибка: $1 не является целым числом."
    exit 1
fi

number=$1

# Проверка на четность
if ((number % 2 == 0)); then
    echo "Четное число"
    exit 0
else
    echo "Нечетное число"
    exit 0
fi