#!/bin/bash

check_dir() {
  # Принимает 1 аргумент — путь к директории
  # Проверяет, что аргумент передан
  # Проверяет, существует ли директория
  # Если существует — выводит количество файлов внутри (не рекурсивно)
  # Возвращает 0 при успехе, 1 при ошибке

  # Проверка аргумента
  if [ -z "$1" ]; then
    echo "Ошибка: нет аргумента"
    return 1
  fi
  
  # Проверка директории
  if [ ! -d "$1" ]; then
    echo "Ошибка: директория не существует"
    return 1
  fi

  # Подсчёт файлов в директории
  ls -1 "$1" | wc -l
  return 0
}

# Тест
check_dir .tests         # Вывод: количество файлов
check_dir /nonexistent   # Ошибка
check_dir                # Ошибка